type BaseValue = {
	name: string;
	nameRus: string;
	hp: string;
	damage: string;
	detection: string;
	str: React.JSX.Element | string;
	strStun: React.JSX.Element | string;
	orb?: string;
	ability: string;
	surviving: string;
	about: string;
	image: any[];
};

type Monster = BaseValue & {
	orb: string;
	image: React.JSX.Element;
	tier: number;
};

const orb2value = (e: string | undefined, tier: number) => {
	switch (e ?? tier) {
		case "s": case 1: return "Small (2-3k)";
		case "m": case 2: return "Medium (3.5-4.5k)";
		case "l": case 3: return "Large (5.5-7.5k)";
		default: return "None";
	}
}

const postprocess = (e: BaseValue[], tier: number) => e.map(e => ({
	...e,
	orb: orb2value(e.orb, tier),
	image: <>{e.image.map((e, i) => <img key={i} src={e} width="100" height="100" />)} </>,
	tier
} as Monster)).sort((a, b) => stringCompare(a.name, b.name));

